#!/usr/bin/env node
/**
 * Generates THIRD_PARTY_LICENSES.md from npm dependencies using license-checker.
 * Run: node scripts/generate-licenses.js
 * Requires: npx license-checker (install with npm install -D license-checker or run via npx)
 */
const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const OUT_FILE = path.join(ROOT, 'THIRD_PARTY_LICENSES.md');
const PACKAGE_NAME = require(path.join(ROOT, 'package.json')).name;

function getLicenses() {
  try {
    const out = execSync('npx license-checker --json', {
      encoding: 'utf-8',
      cwd: ROOT,
      maxBuffer: 10 * 1024 * 1024,
    });
    return JSON.parse(out);
  } catch (e) {
    console.error('Run: npm install -D license-checker (or use npx license-checker)');
    throw e;
  }
}

function escapeMarkdown(text) {
  if (!text || typeof text !== 'string') return '';
  return text.replace(/#/g, '\\#');
}

function main() {
  const data = getLicenses();
  const entries = Object.entries(data)
    .filter(([pkg]) => !pkg.startsWith(PACKAGE_NAME + '@'))
    .sort(([a], [b]) => a.localeCompare(b, undefined, { sensitivity: 'base' }));

  const lines = [
    '# Third-Party Licenses and Notices',
    '',
    'This project includes code from the following npm packages. Their respective licenses apply. Full license texts can be found in each package\'s directory under `node_modules/<package>/LICENSE` (or similar).',
    '',
    'Generated by `node scripts/generate-licenses.js`. Re-run after adding/removing dependencies to update.',
    '',
    '---',
    '',
  ];

  for (const [pkg, info] of entries) {
    const name = escapeMarkdown(pkg);
    const license = (info.licenses || 'Unknown').toString();
    const repo = info.repository || '';
    const publisher = info.publisher || '';
    lines.push(`## ${name}`);
    lines.push('');
    lines.push(`- **License:** ${license}`);
    if (repo) lines.push(`- **Repository:** ${repo}`);
    if (publisher) lines.push(`- **Publisher:** ${publisher}`);
    lines.push('');
  }

  fs.writeFileSync(OUT_FILE, lines.join('\n'), 'utf-8');
  console.log(`Wrote ${OUT_FILE} (${entries.length} packages)`);
}

main();
